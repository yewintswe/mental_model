"""# Cross-tabulations Between SES Groups and Mental Health Binary Categories"""

def crosstab_count_percent(df, row_var, col_var):
    count_table = pd.crosstab(
        df[row_var],
        df[col_var],
        dropna=False
    )

    percent_table = pd.crosstab(
        df[row_var],
        df[col_var],
        normalize="index",
        dropna=False
    ) * 100

    percent_table = percent_table.round(2)

    count_long = count_table.reset_index().melt(
        id_vars=row_var,
        var_name="Mental Health Category",
        value_name="Count"
    )

    percent_long = percent_table.reset_index().melt(
        id_vars=row_var,
        var_name="Mental Health Category",
        value_name="Row Percent"
    )

    merged = count_long.merge(
        percent_long,
        on=[row_var, "Mental Health Category"]
    )

    merged = merged.rename(columns={row_var: "Group"})
    merged.insert(0, "Group Variable", row_var)
    merged.insert(2, "Mental Health Variable", col_var)

    return merged

