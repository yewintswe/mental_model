# Function to detect symptom-present values
def is_symptom_present(series):
    # Convert to string to ensure .str accessor works
    return series.astype(str).str.contains('symptoms', case=False, na=False) & \
           ~series.astype(str).str.contains('No/low', case=False, na=False)

